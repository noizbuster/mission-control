#!/usr/bin/env node
/**
 * Bundled agent codegen.
 *
 * Reads `packages/core/src/agents/bundled-src/*.md` and emits TypeScript modules
 * under `packages/core/src/agents/bundled/` so `tsc` can compile the markdown
 * content into the runtime. mission-control uses `tsc` + Node ESM (NOT Bun), so
 * `import ... with { type: 'text' }` is unavailable; this codegen bridges that
 * gap by producing `export default "<markdown>";` modules.
 *
 * Output (deterministic, alphabetical by filename):
 *   bundled/<name>.md.ts   — `export default <JSON.stringify(mdContent)>;`
 *   bundled/index.ts       — imports every module, exports BUNDLED_AGENT_TEMPLATES.
 *
 * Run: `node scripts/generate-bundled-agents.mjs`
 */
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { basename, dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, '..');
const SRC_DIR = join(ROOT, 'packages/core/src/agents/bundled-src');
const OUT_DIR = join(ROOT, 'packages/core/src/agents/bundled');

/** kebab-case base name -> valid camelCase identifier. */
function toIdentifier(base) {
    return base.replace(/-([a-z0-9])/g, (_, ch) => ch.toUpperCase());
}

async function main() {
    await mkdir(OUT_DIR, { recursive: true });

    const entries = await readdir(SRC_DIR, { withFileTypes: true });
    const mdNames = entries
        .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
        .map((entry) => entry.name)
        .sort();

    if (mdNames.length === 0) {
        throw new Error(`No .md files found in ${SRC_DIR}`);
    }

    const modules = [];
    for (const fileName of mdNames) {
        const base = basename(fileName, '.md');
        const content = await readFile(join(SRC_DIR, fileName), 'utf8');
        const moduleFileName = `${base}.md.ts`;
        const tsBody =
            `// AUTO-GENERATED by scripts/generate-bundled-agents.mjs — do not edit.\n` +
            `// Source: packages/core/src/agents/bundled-src/${fileName}\n` +
            `export default ${JSON.stringify(content)};\n`;
        await writeFile(join(OUT_DIR, moduleFileName), tsBody);
        modules.push({ base, moduleFileName, identifier: toIdentifier(base) });
    }

    const importLines = modules
        .map((mod) => `import ${mod.identifier} from './${mod.moduleFileName.replace(/\.ts$/, '.js')}';`)
        .join('\n');

    const arrayEntries = modules.map((mod) => `    ${mod.identifier},`).join('\n');

    const indexBody =
        `// AUTO-GENERATED by scripts/generate-bundled-agents.mjs — do not edit.\n` +
        `${importLines}\n\n` +
        `export const BUNDLED_AGENT_TEMPLATES: readonly string[] = [\n${arrayEntries}\n];\n`;

    await writeFile(join(OUT_DIR, 'index.ts'), indexBody);

    const summary = modules.map((mod) => `  ${mod.moduleFileName}`).join('\n');
    console.log(`Generated ${modules.length} bundled agent module(s) under ${OUT_DIR}:\n${summary}`);
}

main().catch((error) => {
    console.error(error);
    process.exit(1);
});
